package day1;

public class BooleanVariables {
   public static void main(String[] args) {
      /*
       * boolean variables can store TRUE and FALSE
       */

      // declare a boolean variable to represent if a door is locked
      boolean isLocked;

      // declare a boolean variable to represent if a number is odd
      boolean isOdd;

      // declare a boolean variable to represent whether it is raining
      boolean isRaining;

      // declare a boolean variable to represent if a player has a shield
      boolean hasShield;

      // assign values to the variables you declared above
      isLocked = true;
      isOdd = false;
      isRaining = true;
      hasShield = false;

      // print the values to the console with the variable name in front of it
      System.out.println("isLocked is " + isLocked);
      System.out.println("isOdd is " + isOdd);
      System.out.println("isRaining is " + isRaining);
      System.out.println("hasShield is " + hasShield);

   }
}
